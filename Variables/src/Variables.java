public class Variables {
	public static void main(String[] args) {
		final float PI;//this will create constant variable
		PI = 3.14F;
		System.out.println(PI);
		//whole number
		byte userAge = 18;
		long total1 = 12345678900L; //exceeds length of int, must add 'L'
		long total2 = 123456789L;//optional 'L
		long total3 = 123;
		//string
		String userName = "Jianyan";
		char charVar = 'a';
	}
}


